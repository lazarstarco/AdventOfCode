function getNumbersFromSchema(schema) {
  numbers = [];
  for (i = 0; i < schema.length; i++) {
    number = 0;
    x = -1;
    y = -1;
    for (j = 0; j < schema[0].length; j++) {
      digit = +schema[i][j];
      // console.log(digit);
      if (isNaN(digit)) {
        if (number != 0) {
          numbers.push({ x, y, number, len: ("" + number).length });
          number = 0;
          x = -1;
          y = -1;
        }
        continue;
      }
      if (x == -1 && y == -1) {
        x = i;
        y = j;
      }
      if (digit >= 0 && digit <= 9) {
        if (number != 0) {
          number *= 10;
        }
        number += digit;
        if (j == schema[0].length - 1) {
          numbers.push({ x, y, number, len: ("" + number).length });
        }
      }
    }
  }
  return numbers;
}

function checkIfShouldBeAdded(schema, number) {
  x = number.x - 1 < 0 ? 0 : number.x - 1;
  y = number.y - 1 < 0 ? 0 : number.y - 1;
  maxX = number.x + 1 >= schema.length ? schema.length - 1 : number.x + 1;
  maxY =
    number.y + number.len >= schema[0].length
      ? schema[0].length - 1
      : number.y + number.len;

  for (i = x; i <= maxX; i++) {
    for (j = y; j <= maxY; j++) {
      symbol = schema[i][j];
      // console.log({ x, y, maxX, maxY, i, j, symbol });
      if (/[^()\.\d]/.test(symbol)) {
        // console.log(number)
        return true;
      }
    }
  }

  return false;
}

function findNumber(i, j, schemaNumbers) {
  returnNumber = -1;
  schemaNumbers.forEach((sn) => {
    // console.log({ x: sn.x, y: sn.y, len: sn.len, i, j });
    if (sn.x == i && j >= sn.y && j <= sn.y + sn.len - 1) {
      // console.log(sn);
      returnNumber = sn.number;
    }
  });
  return returnNumber;
}

function multiplyNumbers(numbers) {
  toMultiply = numbers
    .filter((item, index) => numbers.indexOf(item) === index)
    .filter((num) => num != -1);
  // console.log(toMultiply);
  return toMultiply.length == 2 ? toMultiply[0] * toMultiply[1] : 0;
}

function checkForNumbers(schema, n, m, schemaNumbers) {
  x = n - 1 < 0 ? 0 : n - 1;
  y = m - 1 < 0 ? 0 : m - 1;
  maxX = n + 1 >= schema.length ? schema.length - 1 : n + 1;
  maxY = m + 1 >= schema[0].length ? schema[0].length - 1 : m + 1;
  // console.log({ x, y, maxX, maxY, n, m });
  surroundingNumbers = [];
  for (let i = x; i <= maxX; i++) {
    for (let j = y; j <= maxY; j++) {
      symbol = schema[i][j];
      // console.log({ i, j, symbol });
      if (/\d/.test(symbol)) {
        surroundingNumbers.push(findNumber(i, j, schemaNumbers));
      }
    }
  }
  // console.log(surroundingNumbers);
  return surroundingNumbers.length < 2
    ? 0
    : multiplyNumbers(surroundingNumbers);
}

function asterixes(schema, schemaNumbers) {
  sum = 0;
  for (let i = 0; i < schema.length; i++) {
    for (let j = 0; j < schema[0].length; j++) {
      if (schema[i][j] == "*") {
        // console.log({ i, j });
        sum += checkForNumbers(schema, i, j, schemaNumbers);
      }
    }
  }
  return sum;
}

